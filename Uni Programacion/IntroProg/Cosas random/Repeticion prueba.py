# Inicializacion de constantes

CANTIDAD_DE_CICLOS = 20 # Define la cantidad de ciclos

# Declaraci√≥n de variables

ciclo = 0 # Variable para guardar en que ciclo esta

# Repeticion

while ciclo < CANTIDAD_DE_CICLOS: # Mientras ciclos sea menor a la cantidad de ciclos definidos
    ciclo += 1 # Sumar un ciclo
    if ciclo == 20: # Si ciclo es igual a 20
        break # Terminar el codigo
    print(ciclo) # Muentra que ciclo va
    print("Termino de loop") # Muestra que termino el loop